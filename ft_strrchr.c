char	*ft_strrchr(const char *i, int c)
{
	char	*lst = (0);

	while (*i)
	{
		if (*i == c)
			lst = (char*)i;
		++i;
	}
	if (lst)
		return (lst);
	if (c == '\0')
		return ((char*)i);
	return (0);
}
