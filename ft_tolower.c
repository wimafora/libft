#include <stdio.h>

int 	ft_tolower(int i)
{
	if (i >= 65 && i <= 122)
	{
	    return(i +32);
	}
}

int main(void)
{
	char alpha = 'a';

	printf("%c", ft_tolower(alpha));
	return(0);
}
